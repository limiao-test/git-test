package com.wang.datatype;

/**
 * 简单数据运算。
 * 
 * 整数运算（int型）。
 * 
 */
public class Data5 {

	/**
	 * 加法运算。
	 */
	public void jia() {
		// 定义变量
		int a = 2;
		int b = 3;
		int c = 0;

		// 运算
		c = a + b;

		// 访问变量
		System.out.println("c=" + c);
	}

	/**
	 * 减法运算。
	 */
	public void jian() {

		// 定义变量
		int a = 10;
		int b = 5;
		int c = 0;

		// 运算
		c = a - b;

		// 访问变量
		System.out.println("c=" + c);
	}

	/**
	 * 乘法运算。
	 */
	public void cheng() {

		// 定义变量
		int a = 4;
		int b = 5;
		int c = 0;

		// 运算
		c = a * b;

		// 访问变量
		System.out.println("c=" + c);
	}

	/**
	 * 除法运算。
	 */
	public void chu() {
		// 定义变量
		int a = 10;
		int b = 2;
		int c = 0;

		// 运算
		c = a / b;

		// 访问变量
		System.out.println("c=" + c);
	}

}
